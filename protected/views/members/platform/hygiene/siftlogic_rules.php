<section class="row-fluid">
<p>
The file being uploaded must be a correctly formatted Comma Separated
Value file (CSV). At a minimum Email is required. We suggest
including CAN-SPAM required fields.
</p>
<p>
Inputfiles need to be syntactically correct CSV files (<a href="http://tools.ietf.org/html/rfc4180" target="_blank">http://tools.ietf.org/html/rfc4180</a>)</p>
<p>
In summary;
</p><ol>
    <li>
    Columns
    must be separated by commas ","</li>
    <li>
    All
    rows must be terminated using CRLF (Windows line endings)</li>
    <li>
    All
    rows must have same number of columns</li>
    <li>
    Values
    containing commas must be quoted with double-quotes (")<br>EG
                    <ul>
                            <li>
                            Before:
                            123 test way, apt #4</li>
                            <li>
                            After:
                            "123 test way, apt #4"</li>
                    </ul>
        </li>
    <li>
    Values
    containing double quotes must be quoted with double-quotes (")
    and the double-quotes within the value must be escaped with a
    double-quote<br>EG
    <ul>
        <li>
        Before:
        test"one</li>
        <li>
        After:
        "test""one"</li>
    </ul>
    </li>
</ol>
<p></p>
<p>
<i>The
input file must contain a header row matching the headers specified
in the <b>Standard Header Values</b> table below. The
header row values are case-sensitive. The only header which can
appear multiple times is 'discard'. See the table “Standard Header
Values” below for supported default headers.</i>
<br>
<i><b>Custom
Headers: </b></i>In addition to the
Standard Headers we support pass-through custom headers. These
headers must be in the form custom_XXX where XXX is definable. For
example, should you wish to pass a shoe size value through you might
use column custom_shoesize. The result would be that any data in that
column in the input file will be available in the output file.
<br>
<i><b>Other
Headers: </b></i>Attempting
to upload a file without a header row, or a header row containing an
unrecognized header will result in an error and the job being
rejected.
</p>
<p>
Each Input file will result in an output archive containing the following
files;
</p><ul>
    <li>
    report.pdf</li>
    <li>
    results.csv</li>
</ul>
